def formateadorDeRUn(listRun):
    """ Funcion que recibe una lista de RUNS en distintos formatos para entregar formato tipo $$$$$$$$-$"""
    for run in listRun:
        if runNormal.match(run) != None:
            print(run)
        elif runSinPuntos.match(run) != None:
            print(run[:-1] + "-" + run[-1:])
        elif runConPuntos.search(run) != None:
            print(run.replace(".", ""))
        else:
            print("Error con el RUN: " + run)



